/*
Anti-obrazac "magični brojevi" odnosi se na situaciju
kada se neposredno koriste brojevi u kodu umjesto 
korištenja imenovanih konstanti ili varijabli koje 
bi ih opisivale. To često odlikuje činjenica da su 
ovi brojevi nedokučivi ili nedovoljno dokumentirani, 
što otežava razumijevanje koda i održavanje. To može 
predstavljati problem jer otežava pronalaženje i 
mijenjanje tih vrijednosti kasnije, te može dovesti 
do grešaka i nesporazuma u kodu.
*/