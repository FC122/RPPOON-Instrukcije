/*
U cemu se ogleda dependency inversion principle u 
sklopu oblikovnog obrasca Strategija
*/

/*
Dependency Inversion Principle (DIP) u kontekstu 
oblikovnog obrasca Strategija odnosi se na to da 
visokorazinski modul (kontekst) ovisi o apstrakciji 
(sučelju) strategije, a ne o konkretnim implementacijama 
strategija. Ovo omogućuje da se strategije zamijene ili 
modificiraju bez izmjena u visokorazinskom modulu.
*/